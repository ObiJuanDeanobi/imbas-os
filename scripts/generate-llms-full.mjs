import { readFile, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { resolve } from 'node:path';
import { isDeepStrictEqual } from 'node:util';

const outputPath = resolve('llms-full.txt');
const checkOnly = process.argv.includes('--check');
const sourcePaths = [
  'README.md',
  'AGENTS.md',
  'skill.md',
  'docs/index.md',
  'docs/roadmap.md',
  'docs/architecture/ai-first-os.md',
  'docs/architecture/dual-surface-information.md',
  'docs/implementation-status.md',
  'docs/setup/local-development.md',
  'docs/setup/android-companion.md',
  'docs/how-to/use-imbas-os.md',
  'docs/how-to/lorekeeper-review.md',
  'docs/release/fresh-system-1.0-gate.md',
  'docs/release/documentation-1.0-gate.md',
  'docs/known-limitations.md'
];

const missing = sourcePaths.filter((sourcePath) => !existsSync(resolve(sourcePath)));
if (missing.length) {
  console.error(`Cannot generate llms-full.txt; missing sources: ${missing.join(', ')}`);
  process.exit(1);
}

const sections = await Promise.all(
  sourcePaths.map(async (sourcePath) => {
    const content = (await readFile(resolve(sourcePath), 'utf8')).trim();
    return ['---', `## Source: ${sourcePath}`, '', content, ''].join('\n');
  })
);

const generated = [
  '# Imbas OS full AI context',
  '',
  'This file concatenates crucial project context for AI tools. It is generated by `npm run docs:llms`; update the source docs, then regenerate this file.',
  '',
  ...sections
].join('\n');

if (checkOnly) {
  const current = existsSync(outputPath) ? await readFile(outputPath, 'utf8') : '';
  if (!isDeepStrictEqual(current, generated)) {
    console.error('llms-full.txt is stale. Run `npm run docs:llms` and commit the result.');
    process.exit(1);
  }
  console.log('llms-full.txt is up to date');
} else {
  await writeFile(outputPath, generated);
  console.log(`generated llms-full.txt from ${sourcePaths.length} source docs`);
}

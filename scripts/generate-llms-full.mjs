import { readFile, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { resolve } from 'node:path';
import { isDeepStrictEqual } from 'node:util';

const outputPath = resolve('llms-full.txt');
const checkOnly = process.argv.includes('--check');
const sourcePaths = [
  'README.md',
  'AGENTS.md',
  'skill.md',
  'docs/index.md',
  'docs/roadmap.md',
  'docs/design/human-facing-vault-shell.md',
  'docs/architecture/ai-first-os.md',
  'docs/architecture/dual-surface-information.md',
  'docs/architecture/human-filesystem-vault.md',
  'docs/architecture/shared-reference-model.md',
  'docs/architecture/wiki-graph-and-agent-capture.md',
  'docs/architecture/wiki-memsocket-boundary.md',
  'docs/architecture/memsocket-adapter.md',
  'docs/file-format.md',
  'docs/implementation-status.md',
  'docs/setup/local-development.md',
  'docs/setup/android-companion.md',
  'docs/how-to/use-artifact-vault.md',
  'docs/private-preview/use-full-imbas-os.md',
  'docs/how-to/lorekeeper-review.md',
  'docs/release/fresh-system-1.0-gate.md',
  'docs/release/documentation-1.0-gate.md',
  'docs/release/html-artifact-vault-alpha-finish-line.md',
  'docs/release/public-alpha-unveil-checklist.md',
  'docs/release/public-alpha-announcement.md',
  'docs/release/v0.1.0-alpha.0.md',
  'docs/known-limitations.md'
];

function normalizeNewlines(value) {
  return value.replace(/\r\n/g, '\n').replace(/\r/g, '\n');
}

const missing = sourcePaths.filter((sourcePath) => !existsSync(resolve(sourcePath)));
if (missing.length) {
  console.error(`Cannot generate llms-full.txt; missing sources: ${missing.join(', ')}`);
  process.exit(1);
}

const sections = await Promise.all(
  sourcePaths.map(async (sourcePath) => {
    const content = normalizeNewlines(await readFile(resolve(sourcePath), 'utf8')).trim();
    return ['---', `## Source: ${sourcePath}`, '', content, ''].join('\n');
  })
);

const generated = [
  '# Imbas OS full AI context',
  '',
  'This file concatenates crucial project context for AI tools. It is generated by `npm run docs:llms`; update the source docs, then regenerate this file.',
  '',
  ...sections
].join('\n');

if (checkOnly) {
  const current = existsSync(outputPath) ? normalizeNewlines(await readFile(outputPath, 'utf8')) : '';
  if (!isDeepStrictEqual(current, generated)) {
    console.error('llms-full.txt is stale. Run `npm run docs:llms` and commit the result.');
    process.exit(1);
  }
  console.log('llms-full.txt is up to date');
} else {
  await writeFile(outputPath, generated);
  console.log(`generated llms-full.txt from ${sourcePaths.length} source docs`);
}
